#!/usr/bin/env node
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = join(dirname(fileURLToPath(import.meta.url)), "..");

function readPackageVersion(relativePath) {
  const packageJsonPath = join(rootDir, relativePath);
  const packageJson = JSON.parse(readFileSync(packageJsonPath, "utf8"));
  const version = packageJson.version?.trim();

  if (!version) {
    throw new Error(`Missing version in ${relativePath}`);
  }

  return version;
}

const versions = {
  web: readPackageVersion("app/package.json"),
  api: readPackageVersion("api/package.json"),
  android: readPackageVersion("android/package.json"),
};

const generatedSource = `// This file is auto-generated by scripts/generate-build-versions.mjs — do not edit manually.

export const WEB_VERSION = ${JSON.stringify(versions.web)};
export const API_VERSION = ${JSON.stringify(versions.api)};
export const ANDROID_APP_VERSION = ${JSON.stringify(versions.android)};
`;

const targets = [
  "app/src/constants/build-versions.generated.ts",
  "api/src/constants/build-versions.generated.ts",
];

for (const relativeTarget of targets) {
  writeFileSync(join(rootDir, relativeTarget), generatedSource, "utf8");
}

console.log(
  `Build versions: web=${versions.web}, api=${versions.api}, android=${versions.android}`,
);
